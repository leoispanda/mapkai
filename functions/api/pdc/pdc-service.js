export function resolveSessionRoster({ modeId, sessionRoster }) {
  if (Array.isArray(sessionRoster) && sessionRoster.length) {
    return sessionRoster.filter((persona) => persona && persona.isParticipant !== false && persona.status !== "disabled" && persona.status !== "archived");
  }
  const mode = pdcModes[modeId] || pdcModes.personal;
  return mode.defaultPersonaIds
    .map((personaId) => pdcPersonaLibrary.find((persona) => persona.id === personaId))
    .filter((persona) => persona && persona.isParticipant !== false && persona.status === "active");
}

export function resolveFacilitator(modeId) {
  const mode = pdcModes[modeId] || pdcModes.personal;
  return pdcPersonaLibrary.find((persona) => persona.id === mode.facilitatorId && persona.isFacilitator) || pdcPersonaLibrary.find((persona) => persona.id === "blue-whale");
}

export async function generatePdcCouncilRecap({ modeId, sessionRoster, userQuestion, isPlaceholder, includeContentDiagnostics = false, env }) {
  const mode = pdcModes[modeId] || pdcModes.personal;
  const roster = resolveSessionRoster({ modeId: mode.id, sessionRoster });
  const facilitator = resolveFacilitator(mode.id);

  return createPdcCouncilRecap({ mode, roster, facilitator, userQuestion, isPlaceholder, includeContentDiagnostics, env });
}

async function createPdcCouncilRecap({ mode, roster, facilitator, userQuestion, isPlaceholder, includeContentDiagnostics, env }) {
  const isCompany = mode.id === "company";
  const trimmedQuestion = String(userQuestion || "").replace(/\s+/g, " ").trim();
  const safeQuestion = trimmedQuestion.length > 520 ? `${trimmedQuestion.slice(0, 520)}...` : trimmedQuestion;
  const personas = roster.map((persona) => toCouncilRoomPersona(persona, mode.id));
  const dialogueResult = await generatePdcDialogue({
    modeId: mode.id,
    modeLabel: mode.label,
    sessionRoster: personas,
    userQuestion: safeQuestion,
    provider: resolveDialogueProvider(env),
    roundNumber: 1,
    phaseType: "A",
    previousSummary: "",
    meetingMemory: null,
    env,
  });
  const facilitatorSummary = dialogueResult.blueWhaleSummary?.text || facilitator?.placeholderSummary || "Blue Whale is summarizing the first layer of tension, risks, opportunities, and next-step questions. Live PDC generation will later deepen this into a structured council process and final decision memo.";

  return {
    title: "Council Recap",
    modeId: mode.id,
    modeLabel: mode.label,
    isPlaceholder: true,
    dialogueProvider: dialogueResult.provider,
    requestedProvider: dialogueResult.requestedProvider || resolveDialogueProvider(env),
    actualProvider: dialogueResult.actualProvider || dialogueResult.provider,
    fallbackUsed: dialogueResult.fallbackUsed === true,
    fallbackReason: dialogueResult.fallbackReason || "",
    providerErrorShort: dialogueResult.providerErrorShort || "",
    jsonParseFailed: dialogueResult.jsonParseFailed === true,
    modelName: dialogueResult.modelName || "",
    ...(includeContentDiagnostics ? { contentDiagnostics: dialogueResult.contentDiagnostics || null } : {}),
    placeholderNotice: isPlaceholder
      ? "Development placeholder output — live PDC API is not connected yet."
      : "Council Recap placeholder output — live final memo generation is not connected yet.",
    councilRoom: {
      title: "PDC Council Room",
      subtitle: "Your decision is placed on the table. The council reviews it from multiple perspectives.",
      decisionOnTable: safeQuestion,
      personas,
      dialogueProvider: dialogueResult.provider,
      currentRoundLabel: dialogueResult.currentRoundLabel,
      dialogue: dialogueResult.dialogue,
      rounds: dialogueResult.rounds,
      facilitator: facilitator
        ? {
            id: facilitator.id,
            name: facilitator.name,
            englishName: facilitator.englishName,
            role: facilitator.role,
            summary: facilitatorSummary,
            summaryTitle: dialogueResult.blueWhaleSummary?.title || "Blue Whale Summary",
          }
        : null,
    },
    recap: buildPlaceholderSections({ isCompany, safeQuestion, personas }),
  };
}

function resolveDialogueProvider(env = {}) {
  const requested = String(env.PDC_DIALOGUE_PROVIDER || "placeholder").trim().toLowerCase();
  return allowedDialogueProviders.has(requested) ? requested : "placeholder";
}

export async function generatePdcDialogue({ modeId, modeLabel, sessionRoster, observerRoster = [], userQuestion, provider, roundNumber = 1, phaseType = "A", previousSummary = "", meetingMemory = null, userIntervention = "", env }) {
  const fallback = createPlaceholderDialogueResult({ modeId, sessionRoster, roundNumber, phaseType, previousSummary, meetingMemory, userIntervention });

  if (provider === "openai") {
    if (!env?.OPENAI_API_KEY) {
      return fallbackDialogueResult({ fallback, requestedProvider: "openai", failedProvider: "openai", reason: "missing OPENAI_API_KEY", providerErrorShort: "OpenAI API key is not configured.", params: { modeId, modeLabel, sessionRoster, userQuestion, roundNumber, phaseType, previousSummary, meetingMemory, userIntervention, env } });
    }
    try {
      return await generateOpenAiDialogue({ modeId, modeLabel, sessionRoster, observerRoster, userQuestion, roundNumber, phaseType, previousSummary, meetingMemory, userIntervention, env });
    } catch (error) {
      const message = String(error.message || "OpenAI call failed");
      console.error("PDC OpenAI dialogue provider failed:", message);
      return fallbackDialogueResult({ fallback, requestedProvider: "openai", failedProvider: "openai", reason: getOpenAiFallbackReason(message), providerErrorShort: message.slice(0, 180), providerDiagnostics: error.pdcDiagnostics || null, params: { modeId, modeLabel, sessionRoster, userQuestion, roundNumber, phaseType, previousSummary, meetingMemory, userIntervention, env } });
    }
  }

  if (provider === "cloudflare") {
    if (!env?.AI) {
      return {
        ...fallback,
        requestedProvider: "cloudflare",
        actualProvider: "placeholder",
        fallbackUsed: true,
        fallbackReason: "missing AI binding",
        providerErrorShort: "Cloudflare Workers AI binding named AI is not configured.",
        jsonParseFailed: false,
        modelName: "",
      };
    }
    try {
      return await generateCloudflareDialogue({ modeId, modeLabel, sessionRoster, userQuestion, roundNumber, phaseType, previousSummary, meetingMemory, userIntervention, env });
    } catch (error) {
      console.error("PDC Cloudflare dialogue provider failed:", error);
      const message = String(error.message || "Cloudflare call failed");
      return {
        ...fallback,
        requestedProvider: "cloudflare",
        actualProvider: "placeholder",
        fallbackUsed: true,
        fallbackReason: getProviderFallbackReason(message),
        providerErrorShort: message.slice(0, 180),
        jsonParseFailed: /json/i.test(message),
        modelName: String(env?.PDC_CLOUDFLARE_MODEL || defaultCloudflareModel),
      };
    }
  }

  return { ...fallback, requestedProvider: "placeholder", actualProvider: "placeholder", fallbackUsed: false, fallbackReason: "", providerErrorShort: "", jsonParseFailed: false, modelName: "" };
}

async function fallbackDialogueResult({ fallback, requestedProvider, failedProvider, reason, providerErrorShort, providerDiagnostics = null, params }) {
  const fallbackProvider = resolveFallbackProvider(params.env, failedProvider);
  if (fallbackProvider === "cloudflare" && params.env?.AI) {
    try {
      const cloudflareResult = await generateCloudflareDialogue(params);
      return {
        ...cloudflareResult,
        requestedProvider,
        actualProvider: "cloudflare",
        fallbackUsed: true,
        fallbackReason: `${failedProvider} unavailable: ${reason}. Used Cloudflare fallback.`,
        providerErrorShort,
      };
    } catch (error) {
      const cloudflareMessage = String(error.message || "Cloudflare fallback failed");
      return {
        ...fallback,
        requestedProvider,
        actualProvider: "placeholder",
        fallbackUsed: true,
        fallbackReason: `${failedProvider} unavailable: ${reason}. Cloudflare fallback failed: ${getProviderFallbackReason(cloudflareMessage)}.`,
        providerErrorShort: cloudflareMessage.slice(0, 180),
        jsonParseFailed: /json/i.test(cloudflareMessage),
        modelName: String(params.env?.PDC_CLOUDFLARE_MODEL || defaultCloudflareModel),
      };
    }
  }
  const diagnosticContent = params.env?.PDC_LATENCY_DIAGNOSTIC === "true" && providerDiagnostics && typeof providerDiagnostics === "object"
    ? { ...(fallback.contentDiagnostics || {}), ...providerDiagnostics }
    : fallback.contentDiagnostics;
  return {
    ...fallback,
    requestedProvider,
    actualProvider: "placeholder",
    fallbackUsed: true,
    fallbackReason: fallbackProvider === "cloudflare" ? `${failedProvider} unavailable: ${reason}. Cloudflare fallback missing AI binding.` : `${failedProvider} unavailable: ${reason}. Used placeholder fallback.`,
    providerErrorShort,
    jsonParseFailed: /json/i.test(providerErrorShort || reason),
    modelName: failedProvider === "openai" ? getOpenAiModel(params.env) : "",
    contentDiagnostics: diagnosticContent,
  };
}

function resolveFallbackProvider(env = {}, failedProvider = "") {
  const fallback = String(env.PDC_DIALOGUE_FALLBACK_PROVIDER || "").trim().toLowerCase();
  if (allowedDialogueProviders.has(fallback) && fallback !== failedProvider) return fallback;
  return failedProvider === "openai" ? "cloudflare" : "placeholder";
}

function getProviderFallbackReason(message) {
  if (/json/i.test(message)) return "invalid JSON";
  if (/no usable lines|missing required|missing.*dialogue/i.test(message)) return "missing required fields";
  if (/vote/i.test(message)) return "vote normalization failed";
  return "Cloudflare call failed";
}

function getOpenAiFallbackReason(message) {
  if (/default template/i.test(message)) return "OpenAI output matched default template";
  if (/template content|template phrase/i.test(message)) return "OpenAI output contained template content";
  if (/json/i.test(message)) return "invalid JSON";
  if (/missing.*dialogue|missing required/i.test(message)) return "OpenAI response missing required dialogue statements";
  if (/401|403|auth|api key/i.test(message)) return "OpenAI authentication failed";
  if (/429|rate/i.test(message)) return "OpenAI rate limit";
  if (/missing OPENAI_API_KEY/i.test(message)) return "missing OPENAI_API_KEY";
  return "OpenAI call failed";
}

export async function generatePdcFinalRecap({ modeId, modeLabel, userQuestion, activeRoster = [], observerRoster = [], latestPhase = null, meetingMemory = null, voteSummary = null, userInterventions = [], provider, env }) {
  const fallback = createPlaceholderFinalRecap({ modeId, modeLabel, userQuestion, activeRoster, observerRoster, latestPhase, meetingMemory, voteSummary, userInterventions });
  if (provider === "openai") {
    if (!env?.OPENAI_API_KEY) {
      return fallbackFinalRecapResult({ fallback, requestedProvider: "openai", failedProvider: "openai", reason: "missing OPENAI_API_KEY", providerErrorShort: "OpenAI API key is not configured.", params: { modeId, modeLabel, userQuestion, activeRoster, observerRoster, latestPhase, meetingMemory, voteSummary, userInterventions, env } });
    }
    try {
      return await generateOpenAiFinalRecap({ modeId, modeLabel, userQuestion, activeRoster, observerRoster, latestPhase, meetingMemory, voteSummary, userInterventions, env });
    } catch (error) {
      console.error("PDC OpenAI final recap failed:", error);
      const message = String(error.message || "OpenAI final recap failed");
      return fallbackFinalRecapResult({ fallback, requestedProvider: "openai", failedProvider: "openai", reason: getFinalRecapFallbackReason(message), providerErrorShort: message.slice(0, 160), params: { modeId, modeLabel, userQuestion, activeRoster, observerRoster, latestPhase, meetingMemory, voteSummary, userInterventions, env } });
    }
  }
  if (provider === "cloudflare" && env?.AI) {
    try {
      return await generateCloudflareFinalRecap({ modeId, modeLabel, userQuestion, activeRoster, observerRoster, latestPhase, meetingMemory, voteSummary, userInterventions, env });
    } catch (error) {
      console.error("PDC Cloudflare final recap failed:", error);
      const message = String(error.message || "Unknown error");
      return { ...fallback, requestedProvider: "cloudflare", fallbackUsed: true, fallbackReason: getFinalRecapFallbackReason(message), providerErrorShort: message.slice(0, 160), jsonParseFailed: /json/i.test(message), modelName: String(env?.PDC_CLOUDFLARE_MODEL || defaultCloudflareModel) };
    }
  }
  if (provider === "cloudflare" && !env?.AI) {
    return { ...fallback, requestedProvider: "cloudflare", fallbackReason: "missing AI binding", providerErrorShort: "Cloudflare Workers AI binding named AI is not configured.", jsonParseFailed: false, modelName: "" };
  }
  return { ...fallback, requestedProvider: provider || "placeholder" };
}

async function fallbackFinalRecapResult({ fallback, requestedProvider, failedProvider, reason, providerErrorShort, params }) {
  const fallbackProvider = resolveFallbackProvider(params.env, failedProvider);
  if (fallbackProvider === "cloudflare" && params.env?.AI) {
    try {
      const cloudflareResult = await generateCloudflareFinalRecap(params);
      return {
        ...cloudflareResult,
        requestedProvider,
        actualProvider: "cloudflare",
        fallbackUsed: true,
        fallbackReason: `${failedProvider} unavailable: ${reason}. Used Cloudflare fallback.`,
        providerErrorShort,
      };
    } catch (error) {
      const cloudflareMessage = String(error.message || "Cloudflare final recap fallback failed");
      return {
        ...fallback,
        requestedProvider,
        actualProvider: "placeholder",
        fallbackUsed: true,
        fallbackReason: `${failedProvider} unavailable: ${reason}. Cloudflare fallback failed: ${getFinalRecapFallbackReason(cloudflareMessage)}.`,
        providerErrorShort: cloudflareMessage.slice(0, 160),
        jsonParseFailed: /json/i.test(cloudflareMessage),
        modelName: String(params.env?.PDC_CLOUDFLARE_MODEL || defaultCloudflareModel),
      };
    }
  }
  return {
    ...fallback,
    requestedProvider,
    actualProvider: "placeholder",
    fallbackUsed: true,
    fallbackReason: fallbackProvider === "cloudflare" ? `${failedProvider} unavailable: ${reason}. Cloudflare fallback missing AI binding.` : `${failedProvider} unavailable: ${reason}. Used placeholder fallback.`,
    providerErrorShort,
    jsonParseFailed: /json/i.test(providerErrorShort || reason),
    modelName: failedProvider === "openai" ? getOpenAiModel(params.env) : "",
  };
}

function getFinalRecapFallbackReason(message) {
  if (/json/i.test(message)) return "final recap JSON parse failed";
  return "Cloudflare final recap unavailable.";
}

