async function callOpenAiJson({ env, model, instructions, prompt, schemaName, schema, maxOutputTokens, diagnostics = null, durationKey = "openAiDurationMs" }) {
  const apiKey = String(env?.OPENAI_API_KEY || "").trim();
  if (!apiKey) throw new Error("missing OPENAI_API_KEY");
  const startedAt = Date.now();
  const includeSafeResponseDiagnostics = env?.PDC_LATENCY_DIAGNOSTIC === "true";
  if (diagnostics) {
    diagnostics.promptCharLength = diagnostics.promptCharLength || prompt.length;
    diagnostics.approximateInputTokenEstimate = diagnostics.approximateInputTokenEstimate || estimateTokenCount(`${instructions}\n${prompt}`);
    diagnostics.schemaName = schemaName;
    diagnostics.strict = true;
    diagnostics.maxOutputTokens = maxOutputTokens;
  }
  const response = await fetch(openAiResponsesEndpoint, {
    method: "POST",
    headers: {
      "Authorization": `Bearer ${apiKey}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model,
      instructions,
      input: prompt,
      store: false,
      max_output_tokens: maxOutputTokens,
      text: {
        format: {
          type: "json_schema",
          name: schemaName,
          strict: true,
          schema,
        },
      },
    }),
  });
  const payload = await response.json().catch(() => ({}));
  if (diagnostics && includeSafeResponseDiagnostics) {
    diagnostics.openAiHttpStatus = response.status;
    diagnostics.openAiResponseStatus = normalizeShortText(payload?.status || `http_${response.status}`, 80);
    diagnostics.openAiIncompleteReason = normalizeShortText(getOpenAiIncompleteReason(payload), 120);
  }
  if (!response.ok) {
    const message = normalizeShortText(payload?.error?.message || `OpenAI HTTP ${response.status}`, 220);
    throw attachPdcDiagnostics(new Error(message || `OpenAI HTTP ${response.status}`), diagnostics);
  }
  let text = "";
  try {
    text = extractOpenAiText(payload);
  } catch (error) {
    throw attachPdcDiagnostics(error, diagnostics);
  }
  if (diagnostics) {
    diagnostics[durationKey] = Date.now() - startedAt;
    diagnostics.outputCharLength = text.length;
    if (includeSafeResponseDiagnostics) {
      diagnostics.openAiOutputTextLength = text.length;
      diagnostics.openAiRawTextFirst200 = sanitizeDiagnosticSnippet(text);
    }
  }
  try {
    return parseJsonObject(text);
  } catch (error) {
    throw attachPdcDiagnostics(error, diagnostics);
  }
}

async function callOpenAiJsonWithRetry({ env, model, instructions, prompt, schemaName, schema, maxOutputTokens, retryMaxOutputTokens, retryInstructions, diagnostics = null }) {
  try {
    return await callOpenAiJson({ env, model, instructions, prompt, schemaName, schema, maxOutputTokens, diagnostics });
  } catch (error) {
    if (!isJsonParseError(error)) throw error;
    const retryPrompt = `${prompt}

JSON repair retry:
${retryInstructions || "Return only one complete valid JSON object for the schema. No markdown or extra text."}`;
    try {
      if (diagnostics) diagnostics.jsonRepairRetryUsed = true;
      return await callOpenAiJson({
        env,
        model,
        instructions: `${instructions} ${retryInstructions || "Return only valid JSON."}`,
        prompt: retryPrompt,
        schemaName,
        schema,
        maxOutputTokens: retryMaxOutputTokens || maxOutputTokens,
        diagnostics,
        durationKey: "jsonRepairRetryDurationMs",
      });
    } catch (retryError) {
      if (isJsonParseError(retryError)) {
        retryError.message = `OpenAI JSON retry failed: ${retryError.message}`;
      }
      throw retryError;
    }
  }
}

function isJsonParseError(error) {
  return /json|expected|unexpected|unterminated|parse|position|missing output_text/i.test(String(error?.message || ""));
}

function extractOpenAiText(payload) {
  if (!payload) return "";
  if (typeof payload.output_text === "string") return payload.output_text;
  const output = Array.isArray(payload.output) ? payload.output : [];
  const parts = [];
  output.forEach((item) => {
    const content = Array.isArray(item?.content) ? item.content : [];
    content.forEach((part) => {
      if (typeof part?.text === "string") parts.push(part.text);
      if (typeof part?.output_text === "string") parts.push(part.output_text);
    });
  });
  const text = parts.join("\n").trim();
  if (text) return text;
  throw new Error("OpenAI response missing output_text");
}

function getOpenAiIncompleteReason(payload) {
  if (!payload || typeof payload !== "object") return "";
  const direct = payload.incomplete_details?.reason || payload.error?.code || payload.error?.type || "";
  if (direct) return direct;
  const output = Array.isArray(payload.output) ? payload.output : [];
  for (const item of output) {
    const reason = item?.incomplete_details?.reason || item?.status_details?.reason || "";
    if (reason) return reason;
  }
  return "";
}

function sanitizeDiagnosticSnippet(value) {
  return normalizeShortText(String(value || "").replace(/Bearer\s+[A-Za-z0-9._~-]+/g, "Bearer [redacted]"), 200);
}

function attachPdcDiagnostics(error, diagnostics) {
  if (diagnostics && typeof diagnostics === "object") {
    error.pdcDiagnostics = { ...diagnostics };
  }
  return error;
}

function parseJsonObject(value) {
  const text = String(value || "").trim();
  if (!text) throw new Error("Empty dialogue response");
  try {
    return JSON.parse(text);
  } catch {
    const cleaned = text.replace(/^```(?:json)?/i, "").replace(/```$/i, "").trim();
    try {
      return JSON.parse(cleaned);
    } catch {
      // continue to extracting the largest JSON-looking object
    }
    const match = cleaned.match(/\{[\s\S]*\}/) || text.match(/\{[\s\S]*\}/);
    if (!match) throw new Error("Dialogue response did not contain JSON");
    const candidate = match[0].replace(/,\s*([}\]])/g, "$1");
    return JSON.parse(candidate);
  }
}

