function normalizeShortText(value, maxLength) {
  return String(value || "").replace(/\s+/g, " ").trim().slice(0, maxLength);
}

function isKnownDefaultStatement(text, persona) {
  const normalized = normalizeTemplateText(text);
  if (!normalized) return false;
  const candidates = [
    persona?.statement,
    persona?.placeholderStatement,
    persona?.placeholderStatementPersonal,
    persona?.placeholderStatementCompany,
  ];
  const canonical = findCanonicalPersona(persona?.id);
  if (canonical) {
    candidates.push(canonical.placeholderStatement, canonical.placeholderStatementPersonal, canonical.placeholderStatementCompany);
  }
  return candidates.filter(Boolean).some((value) => normalizeTemplateText(value) === normalized);
}

function normalizeTemplateText(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[“”]/g, "\"")
    .replace(/[‘’]/g, "'")
    .replace(/\s+/g, " ")
    .trim();
}

function findTemplateContentPhrases(text) {
  const checks = [
    ["Building on the last Blue Whale Summary", /building on the last blue whale summary/i],
    ["I would update the [role] position", /i would update the .* position/i],
    ["keep the next move specific", /keep the next move specific/i],
    ["Council dialogue preview for this private pilot", /council dialogue preview for this private pilot/i],
    ["placeholder continuation", /placeholder continuation/i],
  ];
  return checks.filter(([, pattern]) => pattern.test(String(text || ""))).map(([label]) => label);
}

function normalizeStanceType(value) {
  const stanceType = String(value || "").trim().toLowerCase();
  return ["support", "challenge", "clarify", "build", "update", "position", "response", "defend"].includes(stanceType) ? stanceType : "update";
}

function normalizeVote(value, byId) {
  if (!value || typeof value !== "object") return null;
  const targetSpeakerId = String(value.targetSpeakerId || "").trim();
  if (!byId.has(targetSpeakerId)) return null;
  const persona = byId.get(targetSpeakerId);
  return {
    targetSpeakerId,
    targetSpeakerName: normalizeShortText(value.targetSpeakerName, 80) || persona.englishName || persona.name,
    reason: normalizeShortText(value.reason, 160),
  };
}

function buildSimpleVotesBySpeaker(value, byId) {
  const rows = Array.isArray(value) ? value : [];
  const votes = new Map();
  rows.forEach((row) => {
    const speakerId = normalizeShortText(row?.speakerId, 80);
    if (!byId.has(speakerId)) return;
    const contributionTargetId = normalizeShortText(row?.contributionTargetId, 80);
    const concernTargetId = normalizeShortText(row?.concernTargetId, 80);
    const contributionTarget = byId.get(contributionTargetId);
    const concernTarget = byId.get(concernTargetId);
    votes.set(speakerId, {
      contributionVote: contributionTarget ? {
        targetSpeakerId: contributionTarget.id,
        targetSpeakerName: contributionTarget.englishName || contributionTarget.name,
        reason: normalizeShortText(row?.contributionReason, 160) || "This contribution was useful to the phase.",
      } : null,
      concernVote: concernTarget ? {
        targetSpeakerId: concernTarget.id,
        targetSpeakerName: concernTarget.englishName || concernTarget.name,
        reason: normalizeShortText(row?.concernReason, 160) || "This perspective needs more pressure before the next phase.",
      } : null,
    });
  });
  return votes;
}

function fillMissingVotes({ dialogue, sessionRoster }) {
  const rankedTargets = rankDialogueTargets({ dialogue, sessionRoster });
  return dialogue.map((line, index) => {
    const speakerIndex = Math.max(0, sessionRoster.findIndex((persona) => persona.id === line.speakerId));
    const directTarget = line.targetSpeakerId ? sessionRoster.find((persona) => persona.id === line.targetSpeakerId) : null;
    const stanceType = String(line.stanceType || "").toLowerCase();
    const rankedContributionTarget = rankedTargets.find((persona) => persona.id !== line.speakerId) || sessionRoster[(speakerIndex + 1) % sessionRoster.length] || sessionRoster[0];
    const rankedConcernTarget = rankedTargets.find((persona) => persona.id !== line.speakerId && persona.id !== rankedContributionTarget?.id) || directTarget || sessionRoster[(speakerIndex + 2) % sessionRoster.length] || rankedContributionTarget;
    const contributionTarget = directTarget && ["support", "build"].includes(stanceType) ? directTarget : rankedContributionTarget;
    const concernTarget = directTarget && ["challenge", "clarify", "response", "defend"].includes(stanceType) ? directTarget : rankedConcernTarget;
    return {
      ...line,
      contributionVote: line.contributionVote || {
        targetSpeakerId: contributionTarget.id,
        targetSpeakerName: contributionTarget.englishName || contributionTarget.name,
        reason: "This contribution sharpened the council discussion.",
      },
      concernVote: line.concernVote || {
        targetSpeakerId: concernTarget.id,
        targetSpeakerName: concernTarget.englishName || concernTarget.name,
        reason: "This perspective needs the most pressure before the next phase.",
      },
    };
  });
}

function rankDialogueTargets({ dialogue, sessionRoster }) {
  const byId = new Map(sessionRoster.map((persona) => [persona.id, persona]));
  const counts = new Map();
  dialogue.forEach((line) => {
    const targetId = normalizeShortText(line?.targetSpeakerId, 80);
    if (!byId.has(targetId)) return;
    counts.set(targetId, (counts.get(targetId) || 0) + 1);
  });
  return Array.from(counts.entries())
    .sort((a, b) => b[1] - a[1])
    .map(([id]) => byId.get(id))
    .filter(Boolean);
}

function normalizeVoteSummary(value, dialogue, sessionRoster) {
  const fallback = aggregateVoteSummary({ dialogue, personas: sessionRoster });
  if (!value || typeof value !== "object") return fallback;
  const contributionVotes = normalizeVoteRows(value.contributionVotes, fallback.contributionVotes);
  const concernVotes = normalizeVoteRows(value.concernVotes, fallback.concernVotes);
  const summary = aggregateVoteSummary({ dialogue, personas: sessionRoster });
  summary.contributionVotes = contributionVotes.length ? contributionVotes : summary.contributionVotes;
  summary.concernVotes = concernVotes.length ? concernVotes : summary.concernVotes;
  summary.leadingContributor = normalizeTopVote(value.leadingContributor) || summary.leadingContributor;
  summary.mostPressuredPerspective = normalizeTopVote(value.mostPressuredPerspective) || summary.mostPressuredPerspective;
  summary.suggestedArchivedPerspective = normalizeTopVote(value.suggestedArchivedPerspective) || summary.suggestedArchivedPerspective;
  summary.shouldArchivePerspective = value.shouldArchivePerspective === true ? summary.shouldArchivePerspective : summary.shouldArchivePerspective;
  return summary;
}

function normalizeVoteRows(value, fallback) {
  return Array.isArray(value) ? value.map((row) => ({
    targetSpeakerId: normalizeShortText(row?.targetSpeakerId, 80),
    targetSpeakerName: normalizeShortText(row?.targetSpeakerName, 80),
    count: Number(row?.count) || 0,
    reasons: normalizeShortList(row?.reasons, 5),
  })).filter((row) => row.targetSpeakerId && row.count > 0) : fallback || [];
}

function normalizeTopVote(value) {
  if (!value || typeof value !== "object") return null;
  const speakerId = normalizeShortText(value.speakerId || value.targetSpeakerId, 80);
  if (!speakerId) return null;
  return {
    speakerId,
    speakerName: normalizeShortText(value.speakerName || value.targetSpeakerName, 80),
    count: Number(value.count) || 0,
    reasonSummary: normalizeShortText(value.reasonSummary || value.reason, 180),
  };
}

function normalizeRosterUpdate(value, voteSummary) {
  const fallback = buildRosterUpdate(voteSummary);
  if (!value || typeof value !== "object") return fallback;
  if (value.shouldArchivePerspective !== true) return { shouldArchivePerspective: false, reason: normalizeShortText(value.reason, 200) || fallback.reason };
  const archivedSpeakerId = normalizeShortText(value.archivedSpeakerId, 80) || fallback.archivedSpeakerId;
  if (!archivedSpeakerId) return fallback;
  return {
    shouldArchivePerspective: fallback.shouldArchivePerspective,
    archivedSpeakerId,
    archivedSpeakerName: normalizeShortText(value.archivedSpeakerName, 80) || fallback.archivedSpeakerName,
    reason: normalizeShortText(value.reason, 220) || fallback.reason,
  };
}

function normalizeShortList(value, maxItems) {
  return Array.isArray(value)
    ? value.map((item) => normalizeShortText(item, 120)).filter(Boolean).slice(0, maxItems)
    : [];
}

function normalizeMemberStates(value, sessionRoster) {
  const source = value && typeof value === "object" ? value : {};
  return Object.fromEntries(sessionRoster.map((persona) => {
    const state = source[persona.id] && typeof source[persona.id] === "object" ? source[persona.id] : {};
    return [persona.id, {
      stance: normalizeShortText(state.stance, 160),
      supports: normalizeShortList(state.supports, 4),
      challenges: normalizeShortList(state.challenges, 4),
      lastContribution: normalizeShortText(state.lastContribution, 180),
      changedMind: state.changedMind === true,
    }];
  }));
}

