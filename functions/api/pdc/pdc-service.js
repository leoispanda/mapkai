export function resolveSessionRoster({ modeId, sessionRoster }) {
  if (Array.isArray(sessionRoster) && sessionRoster.length) {
    return sessionRoster.filter((persona) => persona && persona.isParticipant !== false && persona.status !== "disabled" && persona.status !== "archived");
  }
  const mode = pdcModes[modeId] || pdcModes.personal;
  return mode.defaultPersonaIds
    .map((personaId) => pdcPersonaLibrary.find((persona) => persona.id === personaId))
    .filter((persona) => persona && persona.isParticipant !== false && persona.status === "active");
}

export function resolveFacilitator(modeId) {
  const mode = pdcModes[modeId] || pdcModes.personal;
  return pdcPersonaLibrary.find((persona) => persona.id === mode.facilitatorId && persona.isFacilitator) || pdcPersonaLibrary.find((persona) => persona.id === "blue-whale");
}

export async function generatePdcCouncilRecap({ modeId, sessionRoster, userQuestion, isPlaceholder, env }) {
  const mode = pdcModes[modeId] || pdcModes.personal;
  const roster = resolveSessionRoster({ modeId: mode.id, sessionRoster });
  const facilitator = resolveFacilitator(mode.id);

  return createPdcCouncilRecap({ mode, roster, facilitator, userQuestion, isPlaceholder, env });
}

async function createPdcCouncilRecap({ mode, roster, facilitator, userQuestion, isPlaceholder, env }) {
  const isCompany = mode.id === "company";
  const trimmedQuestion = String(userQuestion || "").replace(/\s+/g, " ").trim();
  const safeQuestion = trimmedQuestion.length > 520 ? `${trimmedQuestion.slice(0, 520)}...` : trimmedQuestion;
  const personas = roster.map((persona) => toCouncilRoomPersona(persona, mode.id));
  const dialogueResult = await generatePdcDialogue({
    modeId: mode.id,
    modeLabel: mode.label,
    sessionRoster: personas,
    userQuestion: safeQuestion,
    provider: resolveDialogueProvider(env),
    roundNumber: 1,
    phaseType: "A",
    previousSummary: "",
    meetingMemory: null,
    env,
  });
  const facilitatorSummary = dialogueResult.blueWhaleSummary?.text || facilitator?.placeholderSummary || "Blue Whale is summarizing the first layer of tension, risks, opportunities, and next-step questions. Live PDC generation will later deepen this into a structured council process and final decision memo.";

  return {
    title: "Council Recap",
    modeId: mode.id,
    modeLabel: mode.label,
    isPlaceholder: true,
    dialogueProvider: dialogueResult.provider,
    placeholderNotice: isPlaceholder
      ? "Development placeholder output — live PDC API is not connected yet."
      : "Council Recap placeholder output — live final memo generation is not connected yet.",
    councilRoom: {
      title: "PDC Council Room",
      subtitle: "Your decision is placed on the table. The council reviews it from multiple perspectives.",
      decisionOnTable: safeQuestion,
      personas,
      dialogueProvider: dialogueResult.provider,
      currentRoundLabel: dialogueResult.currentRoundLabel,
      dialogue: dialogueResult.dialogue,
      rounds: dialogueResult.rounds,
      facilitator: facilitator
        ? {
            id: facilitator.id,
            name: facilitator.name,
            englishName: facilitator.englishName,
            role: facilitator.role,
            summary: facilitatorSummary,
            summaryTitle: dialogueResult.blueWhaleSummary?.title || "Blue Whale Summary",
          }
        : null,
    },
    recap: buildPlaceholderSections({ isCompany, safeQuestion, personas }),
  };
}

function resolveDialogueProvider(env = {}) {
  const requested = String(env.PDC_DIALOGUE_PROVIDER || "placeholder").trim().toLowerCase();
  return allowedDialogueProviders.has(requested) ? requested : "placeholder";
}

export async function generatePdcDialogue({ modeId, modeLabel, sessionRoster, userQuestion, provider, roundNumber = 1, phaseType = "A", previousSummary = "", meetingMemory = null, userIntervention = "", env }) {
  const fallback = createPlaceholderDialogueResult({ modeId, sessionRoster, roundNumber, phaseType, previousSummary, meetingMemory, userIntervention });

  if (provider === "cloudflare") {
    if (!env?.AI) return fallback;
    try {
      return await generateCloudflareDialogue({ modeId, modeLabel, sessionRoster, userQuestion, roundNumber, phaseType, previousSummary, meetingMemory, userIntervention, env });
    } catch (error) {
      console.error("PDC Cloudflare dialogue provider failed:", error);
      return fallback;
    }
  }

  if (provider === "openai") {
    // TODO: Add server-side OpenAI dialogue provider later.
    return fallback;
  }

  return fallback;
}

export function toCouncilRoomPersona(persona, modeId) {
  return {
    id: persona.id,
    name: persona.name,
    englishName: persona.englishName,
    role: persona.role,
    responsibility: persona.responsibility,
    statement: persona.id === "orion-zhuge"
      ? modeId === "company"
        ? persona.placeholderStatementCompany
        : persona.placeholderStatementPersonal
      : persona.placeholderStatement,
  };
}

function buildDialogueLine({ speakerId, text, personas, stanceType = "update", targetSpeakerId = "", stanceSummary = "" }) {
  const speaker = personas.find((persona) => persona.id === speakerId);
  const target = targetSpeakerId ? personas.find((persona) => persona.id === targetSpeakerId) : null;
  if (!speaker) return null;
  return {
    speakerId,
    speakerName: speaker.englishName || speaker.name,
    speakerChineseName: speaker.name && speaker.name !== speaker.englishName ? speaker.name : "",
    speakerLocalName: speaker.name && speaker.name !== speaker.englishName ? speaker.name : "",
    role: speaker.role,
    stanceType,
    targetSpeakerId: target?.id || "",
    targetSpeakerName: target ? target.englishName || target.name : "",
    text,
    stanceSummary: stanceSummary || text,
  };
}

function buildPlaceholderRound({ id, label, roundNumber, phaseType, entries, summary, personas, convergenceLevel = "low", shouldConsiderStopping = false, suggestedReasonToStop = "" }) {
  const activeTensions = phaseType === "A"
    ? ["current position", "strongest reason", "what matters now"]
    : ["trade-offs", "blind spots", "next clarification"];
  return {
    id,
    label,
    phaseLabel: label,
    roundNumber,
    phaseType,
    canContinue: true,
    canStopAndSummarize: true,
    dialogue: entries.map((entry) => buildDialogueLine({ ...normalizePlaceholderEntry(entry), personas })).filter(Boolean),
    blueWhaleSummary: {
      title: "Blue Whale Summary",
      text: summary,
      strongestDisagreement: phaseType === "B" ? "Risk-first members challenged optimism-first members on timing and downside." : "The council is still mapping the main tension.",
      influenceShift: phaseType === "B" ? "Risk and execution views gained influence." : "Evidence and desire views established the opening frame.",
      unresolvedQuestion: phaseType === "B" ? "Which trade-off should govern the next move?" : "Which position deserves the strongest test?",
      convergenceLevel,
      shouldConsiderStopping,
      suggestedReasonToStop,
    },
    meetingMemory: {
      compactSummary: summary,
      activeTensions,
      phaseHistorySummary: summary,
      mainTension: activeTensions[0] || "",
      activeDisagreements: phaseType === "B" ? ["optimism versus risk", "clarity versus speed"] : [],
      convergenceLevel,
      memberStates: buildMemberStates({ personas, entries, phaseType }),
      strongestViews: personas.slice(0, 3).map((persona) => persona.role),
      openQuestions: phaseType === "A" ? ["Which position has the strongest reason?"] : ["Which trade-off needs the next clarification?"],
      convergenceSignals: shouldConsiderStopping ? ["Several perspectives are pointing toward a narrower decision frame."] : [],
    },
  };
}

function normalizePlaceholderEntry(entry) {
  if (Array.isArray(entry)) {
    const [speakerId, text, stanceType = "update", targetSpeakerId = "", stanceSummary = ""] = entry;
    return { speakerId, text, stanceType, targetSpeakerId, stanceSummary };
  }
  return entry || {};
}

function buildMemberStates({ personas, entries, phaseType }) {
  const normalizedEntries = entries.map(normalizePlaceholderEntry);
  return Object.fromEntries(personas.map((persona) => {
    const entry = normalizedEntries.find((item) => item.speakerId === persona.id) || {};
    return [persona.id, {
      stance: entry.stanceSummary || entry.text || persona.statement || "",
      supports: entry.stanceType === "support" || entry.stanceType === "build" ? [entry.targetSpeakerId].filter(Boolean) : [],
      challenges: entry.stanceType === "challenge" ? [entry.targetSpeakerId].filter(Boolean) : [],
      lastContribution: entry.text || "",
      changedMind: phaseType === "A" && Boolean(entry.targetSpeakerId),
    }];
  }));
}

